// 获取所有标签
function getAllTags() {
    const tagsSet = new Set();
    Object.values(articlesData).forEach(article => {
        if (article.tags) {
            article.tags.forEach(tag => tagsSet.add(tag));
        }
    });
    return Array.from(tagsSet).sort();
}

